    private void limpiarCampos() {
        jtxtCedula.setText("");
        jtxtApellido.setText("");
        jtxtNombre.setText("");
        jtxtTelefono.setText("");
        jtxtEmail.setText("");
        jtxtClave.setText(""); 
    }
    
    private void registrarCliente() {
        HttpClient cliente = new HttpClient(new OnHttpRequestComplete() {
            @Override
            public void onComplete(Response status) {
                if (status.isSuccess()) {
                    JOptionPane.showMessageDialog(null, "La actividad se a registrado correctamente");
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al conectarse con el servidor");
                }
            }
        });
        //if (validarCamposNoVacios()) {
            String ced_cli = jtxtCedula.getText();
            String nom_cli = jtxtNombre.getText();
            String ape_cli = jtxtApellido.getText();
            String tel_cli = jtxtTelefono.getText();
            String email_cli = jtxtEmail.getText();
            String passw_cli = jtxtClave.getText();
            String urlString = services.registrarCliente(ced_cli, nom_cli, ape_cli, tel_cli, email_cli, passw_cli);
            cliente.excecute(urlString.replace(" ", "%20"));
        //} else {
            //JOptionPane.showMessageDialog(null, "No se permiten campos vacios");
        //}
    }
    
    private boolean validarCamposNoVacios() {
        return (jtxtCedula.getText().equals("") && !jtxtNombre.getText().equals("")
                && !jtxtApellido.getText().equals("") && !jtxtTelefono.getText().equals("")
                && !jtxtEmail.getText().equals("") && jtxtClave.getText().equals("")) ? true : false;
    }
